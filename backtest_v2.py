def point_biserial(binary, continuous):
    n = len(binary)
    if n < 2:
        return float("nan")
    n1 = sum(binary)
    n0 = n - n1
    if n1 == 0 or n0 == 0:
        return float("nan")
    m1 = sum(c for b, c in zip(binary, continuous) if b == 1) / n1
    m0 = sum(c for b, c in zip(binary, continuous) if b == 0) / n0
    try:
        s = statistics.stdev(continuous)
    except statistics.StatisticsError:
        return float("nan")
    if s == 0:
        return float("nan")
    return (m1 - m0) / s * math.sqrt(n1 * n0 / n**2)


def sharpe_like(returns):
    if len(returns) < 2:
        return float("nan")
    mu  = sum(returns) / len(returns)
    std = statistics.stdev(returns)
    if std == 0:
        return float("nan")
    return mu / std * math.sqrt(13)


def pct_md(x, decimals=2):
    if math.isnan(x):
        return "n/a"
    return f"{x*100:+.{decimals}f}%"

def pct_s(x, decimals=2):
    return f"{x*100:+.{decimals}f}%"


