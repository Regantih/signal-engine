def run_backtest_v2():
    all_trades: List[TradeV2] = []
    ticker_stats = {}

    # Portfolio tracking for kill switch analysis.
    # NOTE: The kill switch (Rule 4) is NOT applied as a hard blocker in this
    # backtest because the sequential per-ticker simulation cannot accurately
    # track concurrent open positions by calendar date.  Instead, we track
    # what the portfolio NAV looks like in hindsight and report the periods
    # where the kill switch *would* have fired, for informational purposes.
    # All other risk rules (1, 2, 3, 5) are applied normally.
    #
    # Conviction sizing IS applied: composite score scales position size.
    _all_rets: List[float] = []   # running list of completed trade returns

    exit_rule_counts = {
        "TRAILING_STOP": 0,
        "TAKE_PROFIT_THEN_TRAILING": 0,
        "TAKE_PROFIT_THEN_BREAKEVEN": 0,
        "TAKE_PROFIT_THEN_TIME": 0,
        "MOMENTUM_REVERSAL": 0,
        "TIME_STOP": 0,
        "BREAKEVEN_STOP": 0,
    }

    for ticker, rows in RAW.items():
        dates  = [r[0] for r in rows]
        closes = [r[1] for r in rows]
        vols   = [r[2] for r in rows]

        bh_return = closes[-1] / closes[0] - 1
        ticker_stats[ticker] = {
            "bh_return": bh_return,
            "bh_start":  closes[0],
            "bh_end":    closes[-1],
            "ticker_trades": [],
        }

        for i in range(12, len(closes)):
            c_window = closes[:i+1]
            v_window = vols[:i+1]

            if len(c_window) < 13:
                continue

            mom  = compute_momentum(c_window, v_window)
            mr   = compute_mean_reversion(c_window)
            qual = QUALITY_FIXED[ticker]
            fl   = compute_flow(v_window)
            rsk  = compute_risk(c_window[-12:])
            crow = CROWDING_V2[ticker]

            sc, prob, edge, kelly, action = score_and_prob(mom, mr, qual, fl, rsk, crow)

            if action != "BUY":
                continue

            # ── Conviction sizing (no kill switch block in backtest) ─────────
            # Kill switch is tracked post-hoc; not used as entry blocker here.
            # Use a small placeholder drawdown of 0 so conviction_multiplier
            # applies score-based scaling without the drawdown reduction.
            conv_mult = conviction_multiplier(sc, 0.0)
            # conv_mult will be 0 only if score < 0 (very rare with BUY filter)
            if conv_mult == 0.0:
                conv_mult = 1.0   # floor — always enter if BUY signal fires

            # ── Intra-hold risk management ──────────────────────────────────
            entry_price  = closes[i]
            entry_idx    = i
            hwm          = entry_price    # high water mark
            partial_taken = False
            partial_ret   = 0.0

            exit_price    = None
            exit_idx      = None
            exit_rule     = "TIME_STOP"   # default if no rule fires first
            remainder_ret = 0.0

            max_hold = 6   # Rule 5: max 6 weeks (was 4 in v1)

            for week in range(1, max_hold + 1):
                j = entry_idx + week
                if j >= len(closes):
                    # ran out of data
                    exit_price = closes[-1]
                    exit_idx   = len(closes) - 1
                    exit_rule  = "TIME_STOP"
                    break

                price     = closes[j]
                # update HWM
                hwm       = max(hwm, price)
                pnl_pct   = (price - entry_price) / entry_price * 100
                dd_from_hwm = (hwm - price) / hwm * 100

                # Rule 1: Trailing stop — -3% from HWM
                if dd_from_hwm >= 3.0:
                    exit_price = price
                    exit_idx   = j
                    if partial_taken:
                        exit_rule = "TAKE_PROFIT_THEN_TRAILING"
                    else:
                        exit_rule = "TRAILING_STOP"
                    break

                # Rule 2: Take profit +8% — sell half (only once)
                if not partial_taken and pnl_pct >= 8.0:
                    partial_taken = True
                    partial_ret   = pnl_pct / 100  # raw return on first half
                    # Continue holding the other half — don't exit yet

                # Rule 3: Momentum reversal — 4-week return < -5% AND P&L < 2%
                if j >= 4:
                    four_weeks_ago = closes[j - 4]
                    four_week_ret  = (price - four_weeks_ago) / four_weeks_ago * 100
                    if four_week_ret < -5.0 and pnl_pct < 2.0:
                        exit_price = price
                        exit_idx   = j
                        if partial_taken:
                            exit_rule = "TAKE_PROFIT_THEN_TRAILING"  # closest category
                        else:
                            exit_rule = "MOMENTUM_REVERSAL"
                        break

                # Rule 4: Breakeven stop — after partial taken, price near entry
                if partial_taken and pnl_pct < 0.5:
                    exit_price = price
                    exit_idx   = j
                    exit_rule  = "TAKE_PROFIT_THEN_BREAKEVEN"
                    break

                # Time stop — exit at week 6
                if week == max_hold:
                    exit_price = price
                    exit_idx   = j
                    if partial_taken:
                        exit_rule = "TAKE_PROFIT_THEN_TIME"
                    else:
                        exit_rule = "TIME_STOP"
                    break

            if exit_price is None:
                # Ran to end of data
                exit_price = closes[-1]
                exit_idx   = len(closes) - 1
                if partial_taken:
                    exit_rule = "TAKE_PROFIT_THEN_TIME"
                else:
                    exit_rule = "TIME_STOP"

            # ── Compute blended return ────────────────────────────────────
            remainder_ret = (exit_price - entry_price) / entry_price

            if partial_taken:
                # 50% sold at partial_ret, 50% at remainder_ret
                blended_ret = 0.5 * partial_ret + 0.5 * remainder_ret
            else:
                blended_ret = remainder_ret

            hold_weeks = exit_idx - entry_idx
            win = blended_ret > 0

            exit_rule_counts[exit_rule] = exit_rule_counts.get(exit_rule, 0) + 1

            t = TradeV2(
                ticker=ticker,
                entry_date=dates[entry_idx],
                exit_date=dates[exit_idx],
                entry_price=entry_price,
                exit_price=exit_price,
                ret=blended_ret,
                win=win,
                exit_rule=exit_rule,
                partial_taken=partial_taken,
                partial_ret=partial_ret,
                remainder_ret=remainder_ret,
                hold_weeks=hold_weeks,
                signals=dict(momentum=mom, mean_reversion=mr,
                             quality=qual, flow=fl, risk=rsk, crowding=crow),
                score=sc,
                prob=prob,
                conviction_mult=conv_mult,
            )
            all_trades.append(t)
            ticker_stats[ticker]["ticker_trades"].append(t)

            # Track returns for portfolio analytics (post-hoc)
            _all_rets.append(blended_ret)

    return all_trades, ticker_stats, exit_rule_counts


