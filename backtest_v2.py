# 3.  CONVICTION SIZING (mirrors risk-manager.ts convictionSize)
# ─────────────────────────────────────────────────────────────────────────────

def conviction_multiplier(composite_score: float, portfolio_drawdown_pct: float) -> float:
    """
    Returns a position-size multiplier [0, 1.625] based on conviction and
    current portfolio drawdown. Kill switch at >10% drawdown.
    """
    if portfolio_drawdown_pct >= 10.0:
        return 0.0   # kill switch

    mult = 1.0
    if composite_score > 1.0:
        mult *= 1.3
    elif composite_score > 0.5:
        mult *= 1.1
    elif composite_score < 0:
        mult *= 0.5

    # No screener data in backtest — skip convergence bonus

    if portfolio_drawdown_pct > 8:
        mult *= 0.5
    elif portfolio_drawdown_pct > 5:
        mult *= 0.7

    return mult


